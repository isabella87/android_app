package com.banhuitong.enumerate;

public enum JxInComeDetailType{
	IN(1, "收入"), OUT(2, "支出");
	private int value;
	private String desc;

	JxInComeDetailType(final int value, final String desc) {
		this.value = value;
		this.desc = desc;
	}

	public final int value() {
		return this.value;
	}

	public String desc() {
		return this.desc;
	}

}
